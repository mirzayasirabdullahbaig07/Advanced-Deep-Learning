# ==============================================================
# ‚öôÔ∏è Technical Definition
# ==============================================================

# Deep Learning is part of a broader family of Machine Learning methods
# based on **Artificial Neural Networks** with **Representation Learning**.

# Deep learning algorithms use multiple layers to progressively extract
# higher-level features from raw input data.
#
# Example (in image processing):
#     - Lower layers may detect edges.
#     - Middle layers may detect shapes or textures.
#     - Higher layers may detect objects or faces.


# ==============================================================
# üß© What is Representation Learning?
# ==============================================================

# Representation Learning is a set of techniques that enable a system
# to automatically discover useful features or representations from raw data,
# eliminating the need for manual feature engineering.

# It allows the machine to:
#     - Learn the features
#     - Use them directly for tasks like classification or prediction


# ==============================================================
# ü§ñ Deep Learning vs Machine Learning
# ==============================================================

# Category          Machine Learning                 Deep Learning
# -----------------------------------------------------------------------
# Data Dependency   Performs well with small data     Requires large data
# Hardware          Works on CPU                      Needs GPU/TPU
# Training Time     Short                             Long (weeks/months)
# Feature Selection Manual (by humans)                Automatic (learned)
# Interpretability  Easier to interpret               Harder to interpret

# üëâ Summary:
# - For small datasets ‚Üí Machine Learning performs better.
# - For large datasets ‚Üí Deep Learning outperforms ML models.
# - Deep Learning performance improves as data increases ("data hungry").


