# ==============================================================
# üß† What is Deep Learning?
# ==============================================================

# Deep Learning is a subfield of Artificial Intelligence (AI) and Machine Learning (ML)
# inspired by the structure and functioning of the human brain.
#
# Deep learning algorithms aim to mimic how humans learn and make decisions ‚Äî
# by continuously analyzing data and improving performance using neural networks.

# While most machine learning algorithms rely on statistical techniques,
# deep learning uses a logical approach based on **Artificial Neural Networks (ANNs)**.
# A collection of these interconnected neural networks forms the foundation of deep learning.


# ==============================================================
# üß© Artificial Neural Network (ANN)
# ==============================================================

# An Artificial Neural Network (ANN) is the core of deep learning.
# It consists of multiple interconnected layers of nodes (neurons or perceptrons).

# üîπ Perceptrons (neurons): Represented as small circles in a network diagram.
# üîπ Connections (weights): Represent the strength of influence between neurons.
# üîπ Layers:
#     - Input Layer: Receives the raw input data.
#     - Hidden Layers: Perform computations and extract features.
#     - Output Layer: Produces the final result or prediction.

# The presence of multiple hidden layers makes the network "deep",
# which is why it‚Äôs called **Deep Learning**.


